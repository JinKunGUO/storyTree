const SEED_DATA_DIR = path.join(__dirname, 'seed-data');

function main() {
  const files = fs.readdirSync(SEED_DATA_DIR).filter(f => f.endsWith('.json'));

  let matched = 0;
  let unmatched = 0;
  const unmatchedBooks: string[] = [];

  for (const file of files) {
    const filePath = path.join(SEED_DATA_DIR, file);
    const content = fs.readFileSync(filePath, 'utf-8');
    const data = JSON.parse(content);
    const bookTitle = data.title || file.replace('.json', '');

    if (AUTHOR_DICT[bookTitle]) {
      if (data.author_name && data.author_name === AUTHOR_DICT[bookTitle]) {
        continue; // 已有且相同
      }
      data.author_name = AUTHOR_DICT[bookTitle];
      fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf-8');
      matched++;
    } else {
      unmatched++;
      unmatchedBooks.push(bookTitle);
    }
  }

  console.log(`\n========== 完成 ==========`);
  console.log(`总计: ${files.length} 本书`);
  console.log(`匹配: ${matched} 本（已添加 author_name）`);
  console.log(`未匹配: ${unmatched} 本（将使用管理员作为作者）`);

  if (unmatchedBooks.length > 0) {
    console.log(`\n未匹配的书目（前50）：`);
    unmatchedBooks.slice(0, 50).forEach(b => console.log(`  - ${b}`));
    if (unmatchedBooks.length > 50) {
      console.log(`  ... 还有 ${unmatchedBooks.length - 50} 本`);
    }
  }
}

main();
